class RelationalOperators1
{
 public static void main(String[] args)
  {
    int a,b;
    a=7;
    b=11;

    System.out.println(" a is "  +   a  +  " and  b  is "  +  b);
    System.out.println(a==b);   
    System.out.println(a != b); 
    System.out.println(a > b);  
    System.out.println(a < b);  
    System.out.println(a >= b); 
    System.out.println(a <= b); 
   }
}

    